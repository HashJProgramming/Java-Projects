
public class switch1 {
	
	public static void main (String[] args) {
		
		char grade = 'B';
		
			
			switch(grade){
				
						case 'A':
					System.out.println("Grade = A");
					break;
					
						case 'C':
					System.out.println("Grade = B");
					break;
					
						case 'D':
					System.out.println("Grade = C");
					break;
					
					default:
					System.out.println("Grade = D");
					break;
			}
    }
}